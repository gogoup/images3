package com.images3.core.infrastructure.data;

public class ImageSizeOS {

    private int width;
    private int height;
    
    public ImageSizeOS(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
    
}
