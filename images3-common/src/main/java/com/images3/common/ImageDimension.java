package com.images3.common;

public class ImageDimension {

    private int width;
    private int height;
    
    public ImageDimension(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
    
}
